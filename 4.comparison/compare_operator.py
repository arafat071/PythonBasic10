"""
Greater than operator
"""
def greater_than(a,b):
    return a>b

print(greater_than(1,5))


"""
Greater than or equal
"""
def greater_than_or_equal(a,b):
    return a>=b

print(greater_than_or_equal(10,10))

"""
LESS than or equal
"""
def less_than_or_equal(a,b):
    return a<=b

print(less_than_or_equal(1,10))

"""
equal
"""
